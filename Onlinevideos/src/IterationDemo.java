
public class IterationDemo {
   //while, do while, for , for-each
	public static void main(String[] args) // use while loop for printing anything multiple times
	{
		int i=1;
		while(i<=5)
		{
			System.out.println("Hello");
			i++;
		}
		
		
		int j = 1;
        do
        	
        {
        	System.out.println("Bye");
        	j++;
        	
        }while(j<=5);
        
        
        
        for(int k=1;k<=5;k++)       //use for loop when knows starting and end point
        {
        	System.out.println("Hello");
        }
        
	}

}
